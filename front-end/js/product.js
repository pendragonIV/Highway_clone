document.getElementById("filter__list--option1").style.display = "none";
var x= document.getElementById("filter__list--option1");
function ShowFilter1() {
    if(x.style.display== "none"){
    document.getElementById("filter__list--option1").style.display = "block";
    }
    else{
        document.getElementById("filter__list--option1").style.display = "none";
    }
}

